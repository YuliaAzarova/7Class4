# Определите и выведите на экран числа  между 50 и 300,
# делящихся одновременно на 5  и 11

a = []
for j in range(50, 301, 5):
    if j % 11 == 0:
        a.insert(0, j)
a.reverse()
print("числа кратные 5 и 11: ", end="")
for i in range(0, len(a)):
    print(a[i], end=" ")

